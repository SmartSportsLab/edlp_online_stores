SUPPORTED_LANG_CODES = frozenset(_STRINGS.keys())


def tr(lang: str | None, key: str, **kwargs) -> str:
    lang = (lang or _DEFAULT) if (lang or _DEFAULT) in _STRINGS else _DEFAULT
    base = _STRINGS.get(lang, _STRINGS[_DEFAULT])
    s = base.get(key) or _STRINGS[_DEFAULT].get(key, key)
    if kwargs:
        try:
            return s.format(**kwargs)
        except (KeyError, ValueError):
            return s
    return s


def normalize_lang(lang: str | None) -> str:
    """Return a supported UI language code; default Spanish."""
    if lang in SUPPORTED_LANG_CODES:
        return lang
    return _DEFAULT


def club_dropdown_options(lang: str | None, clubs: list):
    all_label = tr(lang, 'all_clubs')
    return [{'label': all_label, 'value': 'Todos los Clubes'}] + [
        {'label': c, 'value': c} for c in clubs
    ]
